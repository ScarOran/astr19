#point numbers, the difference between two integers
#and the product of a floating point number and an integer.
#In each case, have the program print out the data type
#of the resulting answer

#defining the floating numbers 
float_one = 12.0
float_two = 5.0

#defining the integers
int_one = 12
int_two = 5

#defining float and integer
float_mult = 7.0
int_mult = 8


#the sum of two floats
print(float_one, '+', float_two, '=', float_one + float_two)

#the difference of two integers
print(int_one, '-', int_two, '=', int_one - int_two)

#the product of a float and integer
print(float_mult, '*', int_mult, '=', float_mult * int_mult)
